package com.class09;

public class Odometer {

	public static void main(String[] args) {
		for (int o=0; o<=9; o++) {
			for (int b=0; b<=9; b++) {
				for (int c=0; c<=9; c++) {
					for (int d=0; d<=9; d++) {
						System.out.println(o + " "+ b+ " " + c+ " " + d);
		
					}
				}
			}
		} 

	}

}
