package com.classRepl;

public class repl73Arrays {

	public static void main(String[] args) {
		/*
		 * For you to do: Write a program that creates an array of integers that stores
		 * the following values: 45, 78, 12, 67, 55 and then prints all array values.
		 * 
		 * Output: 45 78 12 67 55
		 */
		int[] nums = new int[5];

		nums[0] = 45;
		nums[1] = 78;
		nums[2] = 12;
		nums[3] = 67;
		nums[4] = 55;
		
		System.out.println(nums[0] + " " + nums[1] + " " +nums[2] +" " +nums[3] +" " +nums[4] );
	}

}
